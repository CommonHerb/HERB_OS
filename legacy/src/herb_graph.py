class ConstraintKind(Enum):
    """Types of constraints."""
    INVARIANT = auto()
    PRECONDITION = auto()
    POSTCONDITION = auto()
    DERIVED = auto()
    MAINTENANCE = auto()


@dataclass
class Constraint:
    """
    A constraint is a condition that must hold (or should hold for goals).
    Full constraint expression language is in GRAPH_CONSTRAINTS.md.
    For now, we use Python callables as a placeholder.
    """
    entity_id: int
    name: str
    kind: ConstraintKind
    # For now: a Python function that takes (graph, context) and returns bool
    # Later: proper ConstraintExpr AST
    check_fn: Any  # Callable[[Graph, Dict], bool]
    delta_type_id: Optional[int] = None  # For preconditions/postconditions

    # For DERIVED constraints: function that computes new deltas
    # Signature: (graph, dirty_relations) -> List[PendingDelta]
    derive_fn: Any = None

    # For MAINTENANCE constraints: function that generates repair deltas
    # Signature: (graph) -> List[PendingDelta]
    repair_fn: Any = None

    # Dependencies: which relation types does this constraint read from?
    # Used for incremental checking
    depends_on_relations: List[int] = field(default_factory=list)


