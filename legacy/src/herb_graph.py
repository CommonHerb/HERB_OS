"""
HERB v2: The Graph

This is the core implementation of the graph-based program representation.
The graph IS the program — there is no separate source code.

See GRAPH_REPR.md for the design specification.
"""

from __future__ import annotations
from dataclasses import dataclass, field
from enum import Enum, auto
from typing import Dict, List, Set, Optional, Any, Tuple as PyTuple, Union
import time


# =============================================================================
# VALUES
# =============================================================================

class ValueType(Enum):
    """The primitive value types."""
    INT = auto()
    FLOAT = auto()
    STRING = auto()
    BOOL = auto()
    ENTITY_REF = auto()
    NULL = auto()


@dataclass(frozen=True)
class Value:
    """A primitive value in the graph."""
    type: ValueType
    data: Any  # int, float, str, bool, int (entity_id), or None

    @staticmethod
    def int(n: int) -> Value:
        return Value(ValueType.INT, n)

    @staticmethod
    def float(n: float) -> Value:
        return Value(ValueType.FLOAT, n)

    @staticmethod
    def string(s: str) -> Value:
        return Value(ValueType.STRING, s)

    @staticmethod
    def bool(b: bool) -> Value:
        return Value(ValueType.BOOL, b)

    @staticmethod
    def entity_ref(entity_id: int) -> Value:
        return Value(ValueType.ENTITY_REF, entity_id)

    @staticmethod
    def null() -> Value:
        return Value(ValueType.NULL, None)

    def __repr__(self):
        if self.type == ValueType.NULL:
            return "null"
        elif self.type == ValueType.ENTITY_REF:
            return f"@{self.data}"
        elif self.type == ValueType.STRING:
            return f'"{self.data}"'
        else:
            return str(self.data)


# =============================================================================
# ENTITIES
# =============================================================================

@dataclass
class Entity:
    """
    An entity in the graph. Entities don't have properties — they participate
    in relations.
    """
    id: int
    type_id: int      # Reference to the entity type (also an entity)
    level: int        # 0=instance, 1=type, 2=meta-type, 3=system
    created_at: int   # Tick when created

    def __hash__(self):
        return hash(self.id)

    def __eq__(self, other):
        return isinstance(other, Entity) and self.id == other.id


# =============================================================================
# RELATION TYPES
# =============================================================================

@dataclass
class RoleDefinition:
    """Definition of a role in a relation type."""
    name: str
    value_type: ValueType         # What kind of value this role holds
    entity_type_id: Optional[int] # If ENTITY_REF, what type of entity (None = any)


@dataclass
class RelationType:
    """
    A relation type defines the structure of a kind of connection.
    Relation types are themselves entities (at level 2).
    """
    entity_id: int                  # The entity representing this relation type
    name: str
    roles: List[RoleDefinition]
    role_indices: Dict[str, int]    # role_name -> index for fast lookup

    def __post_init__(self):
        self.role_indices = {r.name: i for i, r in enumerate(self.roles)}

    @property
    def arity(self) -> int:
        return len(self.roles)


# =============================================================================
# TUPLES
# =============================================================================

@dataclass
class Tuple:
    """
    A tuple is an instance of a relation — a specific connection between entities.
    """
    id: int
    type_id: int              # Which RelationType
    values: List[Value]       # One value per role, in role order
    valid_from: int           # Tick when this tuple became valid
    valid_to: Optional[int]   # Tick when this tuple was retracted (None if still valid)

    def is_valid_at(self, tick: int) -> bool:
        return self.valid_from <= tick and (self.valid_to is None or tick < self.valid_to)

    def is_current(self) -> bool:
        return self.valid_to is None


# =============================================================================
# DELTAS
# =============================================================================

class DeltaKind(Enum):
    """The kinds of changes that can happen."""
    ASSERT = auto()   # Add a tuple
    RETRACT = auto()  # Remove a tuple
    MODIFY = auto()   # Change values in a tuple


class DeltaSource(Enum):
    """Where a delta originated from."""
    EXTERNAL = auto()      # From outside the system (user input, etc.)
    CONSTRAINT = auto()    # Generated by constraint maintenance
    GOAL = auto()          # Generated by goal pursuit
    DERIVED = auto()       # Generated by derived constraint propagation


class ViolationPolicy(Enum):
    """What to do when a delta violates a constraint."""
    REJECT = auto()        # Don't apply the delta
    APPLY_AND_REPAIR = auto()  # Apply, let propagation fix
    # TRANSACTION handled separately with transaction_group


@dataclass
class Delta:
    """
    A delta is a change to the graph. Deltas are the primitive — state is
    derived by applying deltas.
    """
    id: int
    kind: DeltaKind
    tuple_id: int                     # The tuple being affected
    relation_type_id: int             # Which relation type
    values: List[Value]               # The tuple values (for ASSERT) or new values (for MODIFY)
    old_values: Optional[List[Value]] # Previous values (for MODIFY/RETRACT)
    tick: int                         # When this delta occurred
    micro: int                        # Sub-tick ordering
    provenance: Provenance            # Why this delta happened


@dataclass
class Provenance:
    """Why a delta happened."""
    cause: str                        # 'external', 'derived', 'goal', 'constraint'
    source_deltas: List[int] = field(default_factory=list)  # Deltas that triggered this
    constraint_id: Optional[int] = None
    goal_id: Optional[int] = None
    description: str = ""


@dataclass
class PendingDelta:
    """
    A delta waiting to be applied during resolution.

    Pending deltas are queued rather than applied immediately, allowing:
    - Batching (multiple deltas in one resolution round)
    - Ordering by priority and causal dependencies
    - Validation before committing
    """
    # What to do
    kind: DeltaKind
    relation_type_id: int
    values: List[Value]

    # For RETRACT/MODIFY: which tuple
    tuple_id: Optional[int] = None

    # Ordering
    priority: int = 0                 # Higher = applied first
    causal_deps: List[int] = field(default_factory=list)  # Delta IDs that must be applied first

    # Metadata
    source: DeltaSource = DeltaSource.EXTERNAL
    source_constraint_id: Optional[int] = None
    source_goal_id: Optional[int] = None
    provenance: Optional[Provenance] = None

    # Violation handling
    violation_policy: ViolationPolicy = ViolationPolicy.REJECT
    transaction_group: Optional[int] = None  # For transactional semantics


class ResolutionStatus(Enum):
    """Result status of a resolution attempt."""
    QUIESCENT = auto()         # Reached stable state
    DIVERGED = auto()          # Hit iteration limit
    CONSTRAINT_VIOLATIONS = auto()  # Some constraints couldn't be satisfied


@dataclass
class ConstraintViolation:
    """Record of a constraint violation during resolution."""
    constraint_id: int
    constraint_name: str
    message: str
    trigger_delta_id: Optional[int] = None  # The delta that caused it
    failed_bindings: Optional[Dict] = None  # What bindings failed


@dataclass
class ResolutionResult:
    """The result of a resolution attempt."""
    status: ResolutionStatus
    iterations: int = 0
    applied_deltas: List[int] = field(default_factory=list)
    rejected_deltas: List[int] = field(default_factory=list)
    violations: List[ConstraintViolation] = field(default_factory=list)
    propagation_cycles: int = 0


