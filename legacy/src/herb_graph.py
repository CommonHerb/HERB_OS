def demo():
    """Demonstrate basic graph operations."""
    print("Creating HERB v2 graph...\n")

    g = Graph()

    # Define entity types
    print("Defining entity types...")
    player_type = g.define_entity_type("player")
    resource_type = g.define_entity_type("resource")

    # Define relation types
    print("Defining relation types...")
    holds = g.define_relation_type("holds", [
        ("holder", ValueType.ENTITY_REF, player_type),
        ("resource", ValueType.ENTITY_REF, resource_type),
        ("amount", ValueType.INT, None)
    ])

    # Create instances
    print("Creating instances...")
    player_1 = g.create_entity(player_type, "player_1")
    gold = g.create_entity(resource_type, "gold")

    # Assert a tuple
    print("Asserting tuple...")
    holds_tuple = g.assert_tuple(holds, {
        "holder": Value.entity_ref(player_1),
        "resource": Value.entity_ref(gold),
        "amount": Value.int(200)
    })

    # Add an invariant
    print("Adding invariant...")
    def gold_nonnegative(graph, ctx):
        # Check all holds tuples have non-negative amount
        for tup in graph.query_tuples(holds):
            amount = tup.values[2]  # amount is third role
            if amount.type == ValueType.INT and amount.data < 0:
                return False
        return True

    g.add_constraint("gold_nonnegative", ConstraintKind.INVARIANT, gold_nonnegative)

    # Query
    print("\nQuerying holds tuples...")
    results = g.query_tuples(holds)
    for tup in results:
        print(f"  {g.render_tuple(tup.id)}")

    # Lookup
    print("\nLooking up player_1's gold...")
    amount = g.lookup(holds, {
        "holder": Value.entity_ref(player_1),
        "resource": Value.entity_ref(gold)
    }, "amount")
    print(f"  Amount: {amount}")

    # Dump state
    print()
    g.dump()

    # Provenance
    print("\n--- Provenance for holds tuple ---")
    for delta in g.why(holds_tuple):
        print(f"  {delta.kind.name} at tick {delta.tick}: {delta.provenance.cause}")

    print("\nDemo complete.")


